        lblPhone.setVisible(true);
        lblUsername1.setVisible(true);
        txtDob.setVisible(true);
        txtEmail.setVisible(true);
        txtFirstname.setVisible(true);
        txtLastname.setVisible(true);
        txtNeuid.setVisible(true);
        txtPassword.setVisible(true);
        txtPhone.setVisible(true);
        txtUsername1.setVisible(true);
        btnSaveUser.setVisible(true);
        
        txtUsername1.setText(loggedInUser.getUsername());
        txtPassword.setText(loggedInUser.getPassword());
        txtFirstname.setText(loggedInUser.getFirstname());
        txtLastname.setText(loggedInUser.getLastname());
