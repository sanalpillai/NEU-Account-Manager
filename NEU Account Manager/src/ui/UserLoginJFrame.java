    private void btnSaveUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveUserActionPerformed
        // TODO add your handling code here:

        if (loggedInUser == null) {
            JOptionPane.showMessageDialog(this, "No user is logged in.");
            return;
        }

        String newPass = txtPassword.getText();

        if (!newPass.equals(loggedInUser.getPassword())) {

            if (passwordHistory.contains(newPass)) {
                JOptionPane.showMessageDialog(this, "Password has already been used. Please choose a different password.");
                return;
            }

            loggedInUser.setPassword(newPass);
            passwordHistory.add(newPass);
        }

        loggedInUser.setUsername(txtUsername1.getText());
        loggedInUser.setFirstname(txtFirstname.getText());
        loggedInUser.setLastname(txtLastname.getText());
        loggedInUser.setNeuid(txtNeuid.getText());
        loggedInUser.setDob(txtDob.getText());
        loggedInUser.setPhone(txtPhone.getText());
        loggedInUser.setEmail(txtEmail.getText());
        
        String username = txtUsername1.getText();
        String password = txtPassword.getText();
        String firstname = txtFirstname.getText();
        String lastname = txtLastname.getText();
        String neuid = txtNeuid.getText();
        String dob = txtDob.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        
        if(username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username cannot be empty.");
            return;
        }

        if(password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password can't be empty.");
            return;
        }

        if(firstname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Firstname cannot be empty.");
            return;
        }

        if(lastname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lastname cannot be empty.");
            return;
        }

        if(neuid.isEmpty() || !neuid.matches("[0-9]+")) {
            JOptionPane.showMessageDialog(this, "NEU ID should be numeric and cannot be empty.");
            return;
        }

        if(!dob.matches("\\d{4}-\\d{2}-\\d{2}")) {
            JOptionPane.showMessageDialog(this, "DOB should be in format YYYY-MM-DD.");
            return;
        }

        if(!phone.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this, "Phone number should be 10 digits.");
            return;
        }

        if(email.isEmpty() || !email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Invalid email address.");
            return;
        }


        JOptionPane.showMessageDialog(this, "Information Updated.");
    }//GEN-LAST:event_btnSaveUserActionPerformed

